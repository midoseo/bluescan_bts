/* ===== empathyMessage.js — 감성터칭 메시지 자동 생성 (테마 B, R25) =====
 * PRD: "관제 신호 발생 시, 신호 맥락에 맞춰 AI가 메시지 문안을 생성하고,
 *       담당자가 톤·타이밍을 점검해 승인 후 발송한다."
 *
 * 실제 서비스 흐름: 이상 발생 시 관제사가 고객에게 "즉시" 연락한다(1차 대응, 이미 완료된 상태).
 * 이 메시지는 그 이후 영업담당자(컨설턴트)가 사후(대략 1~3일 뒤)에 보내는 안부 팔로업이다.
 * 그래서 "화재가 감지됐습니다" 식의 통보 문구가 아니라, 이미 알고 계신 일을 전제로
 * (1) 놀라진 않으셨는지 (2) 잘 해결되었는지 (3) 최근에도 이런 적이 있었는지·처음인지
 * (4) 다른 이슈 있으면 연락 달라는 흐름으로 구성한다.
 *
 * AI는 초안까지만 만든다 — 이 파일은 텍스트 생성만 하고, 실제 발송은 하지 않는다.
 */

const CONTEXT_BY_TYPE = {
  화재: '화재 관련 신호',
  정전: '정전 신호',
  가스누출: '가스 누출 관련 신호',
  누수: '누수 신호',
  고저수위: '저수위(또는 고수위) 신호',
};

export function buildEmpathyMessageDraft(c, signal) {
  const context = CONTEXT_BY_TYPE[signal.type] || `${signal.type} 신호`;
  return `${c.name}님, 안녕하세요. 담당 컨설턴트 ${c.assignedConsultant}입니다.\n` +
    `지난 ${signal.date}에 ${context}가 있어 저희 관제팀에서 바로 연락드렸던 것으로 알고 있는데, 며칠 지나 안부차 다시 연락드립니다.\n\n` +
    `혹시 많이 놀라진 않으셨는지, 그 이후로 잘 해결이 되었는지 여쭤봅니다.\n` +
    `최근에도 비슷한 일이 몇 번 있었는지, 아니면 이번이 처음이셨는지도 궁금합니다.\n\n` +
    `혹시 다른 불편한 점이나 신경 쓰이는 부분이 있으시면 언제든 편하게 연락 주세요. 감사합니다.`;
}
